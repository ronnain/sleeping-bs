	<p>Perdre 1 heure de sommeil, ce n'est clairement pas agréable.</p>
	<p>Contrairement, au changement d'heure d'hiver, ce décalage fait déprimer les grands dormeurs et les couches tard.</p>
	<p>Des études montrent, un taux de déprime, d'accidents et de suicides plus important la semaine qui suit le changement d'heure.</p>
	<p>Heureusement, j'ai 7 astuces faciles à mettre en place pour vous aider à mieux vous adapter sur cette période.</p>
	<p>Si vous les mettez correctement en place, vous ne devrez même pas sentir le changement d'heure.</p>
	<p>Ces conseils sont utiles tant que vous vous sentez fatigué de cette transition.</p>

<h2>Perdre 1h de sommeil</h2>

	<p>Prenons un exemple.</p>
	<p>Habituellement, vous vous couchez à 00h00 et vous mettez un réveil à 7h00.</p>
	<p>Ca vous fait 7h de sommeil.</p>
	<p>Reprenons l'exemple, la nuit du changement d'heure.</p>
	<p>Vous faîte la même chose, vous vous couchez à 00h00 et vous vous levez à 7h00.</p>
	<p>Vous aurez dormi seulement 6h.</p>
	<p>À 2h00 du matin, on avancera directement à 3h00.</p>
	<p>Donc, c'est une heure de sommeil perdue.</p>


	<p>Un des principaux objectifs de ces astuces va être de naturellement vouloir aller au lit plus tôt quand votre corps vous le dira.</p>
	<p>Si vous n'y arrivez pas ce n'est pas grave.</p>
	<p>En suivant ces conseils, vous devriez avoir un sommeil de meilleure qualité et de ressentir des effets positifs.</p>

<h3>Le corps déboussolé</h3>
	<p>Le corps possède une horloge interne calée sur 24h. Elle permet de lancer les différents processus au fur et à mesure de la journée.</p>
	<p>Elle dicte au cerveau quand vous réveiller, quand vous devez manger, vous couchez...</p>
	<p>C'est l'horloge circadienne.</p>
	<p>Elle se base principalement sur le rythme du soleil.</p>
	<p>Elle est une pièce centrale dans le bon fonctionnement de notre organisme.</p>
	<p>Elle aime surtout la régularité.</p>
	<p>Sinon elle est perdue, la pauvre.</p>
	<p>Si tout ne va pas comme elle veut, vous allez ressentir indirectement les conséquences.</p>
	<p>Fatigue, mauvaise humeur...</p>

<h3>Durée d'adaptation du corps</h3>
	<p>Pour une majorité de personne, l'adaptation va se faire au fur et à mesure.</p>
	<p>Au bout d'une semaine, le corps sera adapté à cette nouvelle heure.</p>
	<p>Mais cette durée d'adaptation peut augmenter vos chances d'avoir un accident dû à la fatigue.</p>
	<p>Il est donc intéressant de faire plus attention et d'aider le corps à faire la transition.</p>


<h2>7 Astuces pour ne pas subir 1h de sommeil perdu</h2>

<h3>Astuce 1 - Profiter de la lumière naturelle</h3>
	<p>Pour cela, on va s'appuyer sur le rythme du soleil.</p>
	<p>Comme on l'a vue, l'horloge interne se cale sur le rythme solaire.</p>
	<p>Je vous recommande donc de vous exposer le plus possible à la lumière naturelle surtout l'après-midi et avant que le soleil ne se couche.</p>
	<p>Faite au moins 2 sessions de 15 minutes chacune dont une proche de 18h30, quand le soleil commence à se coucher.</p>
	<p>S'il fait moche, pas de soucis restez proche d'une fenêtre à regarder dehors.</p>
	<p>C'est important que vos yeux reçoivent cette lumière.</p>
	<p>La lumière naturelle même par temps pluvieux est beaucoup plus puissante que la lumière d'intérieur, le corps sait la reconnaître.</p>
	<p>Évitez de rester pas devant un écran.</p>
	<p>Vous pouvez discuter, téléphoner...</p>
	<p>Répétez cette astuce durant la semaine qui suit le changement d'heure.</p>

<h3>Astuce 2 - Favorisez des lumières tamisées dans votre logement</h3>
	<p>Une fois le couché, essayez de limiter les sources de lumière.</p>
	<p>Essayez de garder une ambiance tamisée pour que votre corps comprenne que le soleil s'est couché et qu'il faut ralentir.</p>
	<p>Ca fait permet de faire une démarcation avec les activités de la journée.</p>
	<p>Personnellement, je trouve ça très agréable.</p>
	<p>Je me sens un peu mal quand il y a trop de lumière le soir.</p>
	<p>J'ai l'impression d'avoir trop de sollicitation.</p>

<h3>Astuce 3 - Télé & jeux vidéo - Ok, mais pas trop tard</h3>
<p>Je pense que c'est le sujet le plus sensible pour vous aider à passer cette transition.</p>
	<p>C'est très facile le soir, de regarder la télé et ne plus faire attention à l'heure.</p>
	<p>On peut facilement enchaîner les épisodes, surtout sur Netflix.</p>
	<p>Résultat, on se couche plus tard que prévu.</p>
	<p>Malheureusement, dans notre cas, il va falloir absolument éviter ça.</p>
	<p>Sinon tous vos efforts de la journée tombent à l'eau.</p>
	<br/>
	<p>L'objectif est 1h30 avant votre heure de coucher habituelle, vous arrêtez tous les écrans.</p>
	<p>Le problème des écrans est qu'ils émettent la fameuse lumière bleue qui naturellement provient du soleil.</p>
	<p>Ils vont donc tromper notre horloge interne sur le rythme du soleil, et nous verra aller au lit plus tard.</p>

	<p>À la place, vous pouvez lire, jouer aux jeux de sociétés/cartes, discuter, téléphoner...</p>
	<p>C'est moins cool, mais vous pouvez en profiter pour ranger, nettoyer...</p>

	<p>Vous pouvez aussi simuler ou pas la création d'un bébé.</p>
	<p>Même pratiqué seul, l'orgasme à des effets positifs incroyables sur la santé.</p>
	<p>Et dans notre cas, ça vas nous aider à nous relaxer et dormir plus rapidement.</p>

<h3>Astuce 4 - Soirée spéciale relaxation</h3>

	<p>Pour appeler le sommeil, il faut se détendre.</p>
	<p>Comme dit juste avant, l'orgasme est très bon pour ça.</p>
	<p>Dans l'heure qui précède votre aller au lit.</p>
	<p>Vous pouvez ralentir en faisant un peu de méditation, yoga...</p>
	<p>Pour méditer, c'est très simple.</p>
	<p>Voici ce que je fais :</p>
	<ul>
		<li><p>Je préviens mon entourage et je coupe mon portable, pour ne pas être dérangé pendant la séance.</p></li>
		<li><p>Je lance un chronomètre de 5 min.</p></li>
		<li><p>Je m'assis sur une chaise dans un endroit calme.</p></li>
		<li><p>Je ferme les yeux.</p></li>
		<li><p>Je me concentre sur ma respiration.</p></li>
		<li><p>J'essaie d'avoir une respiration calme et profonde.</p></li>
		<li><p>Je compte mes respirations de 0 à 10 puis de 10 à 0.</p></li>
		<li><p>Dès qu'une pensée arrive, j'essaie de me rendre compte que je ne suis plus concentré sur ma respiration. Puis je reprend à 0 mon comptage.</p></li>
		<li><p>Si, j'arrive à compter de 0 à 10 puis 10 à 0. Je profite du temps restant pour ressentir mon corps détendue.</p></li>
	</ul>
	<p>Si vous ne le sentez pas, vous pouvez utiliser une application gratuite.</p>
	<p>Comme Petit bambou.</p>
	<p>Disponible sur toutes les plateformes mobiles.</p>
	<p>L'application permet de faire de la méditation guidée.</p>

<h3>Astuce 5 - Prendre un bain ou une douche chaude</h3>
	<p>Pendant l'heure de relaxation, vous pouvez prendre un bain chaud ou une douche chaude.</p>
	<p>Ca va permettre d'aider le corps à se refroidir.</p>
	<p>Ca peut paraître contre intuitif.</p>

	<p>Mais le corps agit comme un régulateur de température.</p>
	<p>Si vous, prenez une douche froide. Le corps va se mettre à chauffer davantage pour garder sa température.</p>
	<p>Si vous, prenez une douche chaude. Le corps va se mettre à se refroidir pour contrer le chaud.</p>

	<p>Avant de dormir, le corps expulse la chaleur pour ralentir.</p>
	<p>Souvent, avant de dormir, on peut ressentir ses pieds, ses mains et ses oreilles toutes chaudes.</p>
	<p>C'est que vous entamez le processus de sommeil.</p>

<h3>Astuce 6 - Café & Thé, seulement le matin</h3>
	<p>Peut-être un autre point sensible surtout chez des consommateurs de café.</p>
	<p>Le café et le thé ont des répercussions plusieurs heures après l'ingestion.</p>
	<p>On consomme le café et le thé pour sentir un coup de boost contre la fatigue.</p>
	<p>Bien qu'on ne ressent pas les effets très longtemps.</p>
	<p>Le corps met beaucoup de temps à les éliminer et ça impacte d'autres fonctionnements dont le sommeil.</p>
	<p>Le pire, c'est qu'on ne s'en rend pas compte. Surtout si on est habitué à en consommer.</p>
	<br/>
	<p>Pour cette période, je vous recommande d'éviter le café et le thé.</p>
	<p>Si vous ne pouvez pas faire sans, vous pouvez en prendre 1 tasse le matin.</p>

	<p>Vous sentez fatigué l'après-midi. Ca sera passager, au pire faite une sieste chronométré de 20min pas plus.</p>
	<p>Sinon vous allez vous réveiller déboussolé.</p>

	<p class="citation">PS: Vous pouvez y aller sur le café décaféiné et les infusions.</p>

<h3>Astuce 7 - Le dîner</h3>
	<p>Toujours pour aider votre corps à bien lancer le processus de sommeil.</p>
	<p>Essayez d'espacer de minimum 2h votre dernier repas de l'heure du coucher.</p>
	<p>L'estomac à besoin de beaucoup d'énergie pour digérer.</p>
	<p>Il vaut mieux lui laisser le temps de faire son principal travail.</p>
	<p>Une fois, que l'estomac à fait son job.</p>
	<p>Le corps peut enfin ralentir.</p>
</p>

<h3>Laisser venir le sommeil</h3>
	<p>Quand vous sentez le sommeil vous gagner, n'essayez pas d'attendre votre habituelle de coucher.</p>
	<p>Allez vous coucher.</p>
	<p>Sinon, allez vous coucher comme d'habitude.</p>
	<p>Les actions positives que vous avez fait durant la soirée, vont vous permettre d'avoir un sommeil de meilleure qualité.</p>

