<h2>Découvrir son chronotype</h2>
<h3>Connaitre son groupe de dormeur/chronotype</h3>
<p>Le livre distingue 4 grands groupes de dormeurs / chronotypes. Le terme chronotype correspond au rythme de vie du corps (<a href="https://fr.wikipedia.org/wiki/Chronotype" target="_blank">lien wikipedia</a>).
Les conseils qui sont donnés, sont adaptés aux forces et faiblesses de chaque groupe.
C'est pourquoi, l'auteur propose une première partie avec des questionnaires qui permettent de découvrir quel type de dormeur, on appartient.
N'hésitez pas à répondre au mini quiz présent un peu plus loin dans l'article pour avoir une idée du groupe auquel vous appartenez.
</p>
<h3>La chronobiologie</h3>
<p>Le Dr.Breus utilise comme méthode la chronobiologie/chronorythme. Cette méthode se base sur les horloges biologiques du corps, tous les mécanismes/processus qui se passent dans notre corps.
La chronobiologie étudie, les fluctuations en énergie des organes et les différentes sécrétions d'hormones qui nous permettent de vivre en bonne santé.
Chaque chronotype à des avantages et des désavantages. Le chronotype de chacun est inscrit dans nos gênes. Il vaut mieux être en harmonie avec son propre chronotype plutôt que de se forcer à prendre des habitudes d'un autre groupe.
Être en cohérence avec son chronotype, c'est de maximiser son potentiel, être plus heureux, plus productif, plus énergique, être bien avec soie, et accueillir avec empathie ses faiblesses qui sont finalements là pour aider le corps à bien fonctionner.
</p>
<h3>Les 4 grand groupes de dormeurs alias chronotypes</h3>
<p>On parle souvent de deux écoles, les lèves tôt / couche-tôt et les lèves tard/couche tard.
Et bien, pour ces deux catégories, le chronotype du lion symbolise les lèves tôt/couche tôt et le chronotype du loup symbolise les lèves tards/couches tards.
Le chronotype de l'ours est là pour représenter ceux qui sont de la journée, c'est-à-dire qui ne se lèvent pas spécialement tôt et ne se couche pas spécialement tard.
Ils représentent environ 50% de la population, le reste étant partagé entre les autres chronotypes.
Et enfin, une dernière catégorie, les dauphins.
Ils, ont beaucoup de problèmes de sommeil pour s'endormir et se réveillent souvent dans la nuit, ils souffrent d'insomnie chronique.
</p>
<h3>Cycle des chronotypes</h3>
<p>Suivant l'avancement de notre cycle de vie, notre chronotype va évoluer.
A la naissance, nous n'avons pas encore de chronotype et cela jusqu'au tour des 4 ans où l'enfant va posséder son propre rythme.
Généralement, les jeunes enfants ont tendance à se lever tôt, ils appartiennent donc à la catégorie des lions.
Puis vient l'adolescence où on les observe à se coucher et se lever de plus en plus tard. Ils deviennent des loups.
Enfin, en grandissant à la fin de l'adolescence. Les loups deviennent des ours.
Tout cela est déterminé génétiquement et peut être influencé par notre environnement/habitudes.
Ce n'est pas une fatalité et c'est tout à fait normale. D'être adulte et d'avoir encore un chronotype de loup. Chaque chronotype à joué un rôle important dans la société.
</p>
<h3>L'importance de chronotype varié pour la survie</h3>
<p>L'importance de chaque chronotypes pour la société.
D'après le Dr.Breus chaque chronotypes a une importance pour la survie de la société.
Alors que le soleil se couche, quand les lions et les ours s'endorment, les loups sont eux toujours actifs, voir au pic de leurs formes. Les loups commencent le tour de garde pour surveiller le groupe des envahisseurs potentiels qui attaqueraient pendant la nuit.
Lorsque les loups les plus extrêmes vont se coucher très tard, certains lions commencent à se réveiller et à prendre la relève. Jusqu'au levé du jour où les ours vont se réveiller en pleine forme pour toutes les tâches du camp à effectuer.
Et enfin, on a les dauphins. Leur sommeil est légé, leur permettent de se réveiller au moindre bruit et de donner l'alerte. Ils sont malgré eux les protecteurs du groupe.
</p>
<h3>Déterminer son chronotype</h3>
<p>Le livre parle de tout ce qui influence le sommeil. Lorsque l'on détermine son chronotype, il peut être difficile d'être objectif, car certaines mauvaises habitudes nous font adopter un mauvais comportement qui impacte notre sommeil.
Par exemple, peut-être que vous êtes un ours qui va aller se coucher à 2h du matin après avoir passé la soirée à jouer à des jeux vidéo. Forcément, vous vous lèverez plus tard, ce sera peut-être même difficile si vous devez vous lever tôt pour aller au boulot.
Vous avez peut-être l'impression d'être un loup, mais pouvez être qu'en réalité vous un ours avec de mauvaises habitudes pour le sommeil.
