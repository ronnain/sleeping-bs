<h2>Les chronotypes</h2>
<p>Pour chaque chronotype, le Dr.Breus décrit l'emploie du temps idéale en s'appuyant sur un panel d'études scientifiques. On y apprend pourquoi on devrait faire ça à tel moment, pourquoi on est fatiguée à ces moment là...
Adapter la moindre de ses habitudes à son emploi du temps idéal va rapidement impacter notre ressente au quotidien. Plus on se rapproche de son chronotype, plus on en tire de bénéfices.

Pour régler ses problèmes de sommeil. Il faut réussir à changer ses habitudes en suivant ce qui est recommandé dans le livre. C'est une étape très difficile, surtout lorsque l'on doit changer de vieilles habitudes.
Mais en étant objectif, les problèmes de sommeil viennent en partie de nos habitudes, elles se sont installées avec le temps et malheureusement ne sont pas bonnes pour notre sommeil. Faire un petit effort, va rapidement apporter de grands résultats. Imaginer se réveiller en pleine forme !
Cela va booster notre énergie ce qui va permettre de recommencer la soirée suivante... Vous aurez enfin les clés de votre sommeil.
</p>

<h3>Les lions</h3>
<h3>Exemple de quotidien d'un lion</h3>
<p>Le Dr.Breus s'appuie sur l'exemple de Robert, responsable marketing. Robert décrit son quotidien, il se lève naturellement avant tout le monde où il en profite pour travailler plusieurs heures avant que les premières personnes arrivent au boulot. Cela lui permet de se concentrer sur sa carrière.
Par contre le soir Robert se couche-tôt. Bien qu'il excelle dans son métier, son objectif est de fonder une famille. Mais il est tellement fatigué le soir qu'il n'arrive pas à rencontrer du monde.
</p>
<h3>Autres caractéristiques des lions</h3>
<ul>
  <li>Élève modèle de la classe</li>
  <li>Toujours prêt à travailler plus</li>
  <li>Davantage de chance d'être en meilleure santé, de grimper les échelons</li>
  <li>Souvent, les PDG des grands groupes sont des lions</li>
  <li>Fonceur</li>
  <li>Autorité naturelle</li>
  <li>Très organisé dans leurs habitudes</li>
  <li>Pic d'énergie : le matin</li>
</ul>
<h3>L'objecif du programme des lions</h3>
<p>Leur permettre de profiter au maximum de leurs soirées.</p>
<h3>Exemple de conseil pour un lion</h3>
<p>Préférer faire du sport le soir entre 18h et 19h suivis d'une douche froide.</p>

<h3>Les ours</h3>
<h3>Exemple de quotidien d'un ours</h3>
<p>Ben, père de famille, ayant 20kg en trop, mais en bonne santé. Habité par une fatigue légère et constante. N'a qu'une envie en rentrant le soir, manger et se détendre, il n'arrive pas à se motiver pour s'occuper de ses enfants et des tâches de la maison, se réveille toujours assommé, même après une bonne nuit.
Il fait des grasses matinées et des siestes le week-end et a dû mal à s'endormir le dimanche soir. Fait du sport le week-end, mais se sent mieux s'il en fait la semaine.
</p>
<h3>Autres caractéristiques des ours</h3>
<ul>
  <li>Préfère faire des activités collectives</li>
  <li>Rythme de vie se callant bien avec les normes sociales</li>
  <li>Fort besoin en sommeil au moins 8h par nuit</li>
  <li>N'aime pas suivre un rythme défini.</li>
  <li>Tentation de grignotage nocturne</li>
  <li>Pic d'énergie : la journée</li>
</ul>
<h3>L'objectif du programme pour un ours</h3>
<p>Dormir suffisamment et faire du sport en semaine
Modifier les heures de repas afin d'accélérer leur métabolisme et perdre du poids
Optimiser les siestes et les activités afin de se sentir en pleine forme le soir.
</p>
<h3>Exemple de conseil pour un ours</h3>
<p>Faire l'amour en se réveillant le matin. Pas d'alcool après 20h. Manger le plus gros repas le matin.</p>


<h3>Les loups</h3>
<h3>Exemple de quotidien d'un loup</h3>
<p>Ann, mère de famille, en pleine forme à 17h où la plupart des gens ressentent la fatigue. Elle se couche à minuit, mais elle met plusieurs heures à s'endormir.
Elle est complètement dans les vapes au réveil et se met en pilote automatique. Pour son petit-déjeuner, elle préfère boire 2 tasses de café, car elle n'a pas faim. Elle a beaucoup de mal à travailler avant 11h. Mais en fin d'après-midi où tout le monde commence à fatiguée. Elle est en pleine forme et rattrape sa journée de travail.
</p>
<h3>Autres caractéristiques des loups</h3>
<ul>
  <li>Souvent plus créatif que la moyenne</li>
  <li>Un esprit de rébellion</li>
  <li>Souvent impatient et impulsif</li>
  <li>Insatisfait de leur existence</li>
</ul>
<h3>L'objectif du programme pour un loup</h3>
<p>Améliorer la productivité au travail
Modifier les horaires de repas pour accélérer leur métabolisme
Faire des nuits plus longues
Stabiliser les sauts d'humeur afin d'être davantage satisfait de leur existence.
</p>
<h3>Exemple de conseil pour un loup</h3>
<p>Mettre deux réveils, un premier 20min avant le second (l'heure où se lever). Prendre la première tasse de café à 11h, pas avant.</p>

<h3>Les dauphins</h3>
<h3>Exemple de quotidien d'un dauphin</h3>
<p>Stéphanie, mère d'un étudiant. Décrite comme fatiguée et nerveuse. Elle rêverait de dormir 6 h, mais en fait plutôt 4 ou 5h. Elle se réveille plusieurs fois 5 ou 6 fois par nuit. Elle ne sait pas quand elle se rendort ni si elle se rendort.
Elle dit que c'est encore pire depuis la ménopause. Elle mange beaucoup au réveil, parfois elle oublie de manger le midi. Elle a dû mal à se concentrer le matin mais ça va mieux le midi. Elle se sent le plus en forme le soir vers 20h/21h. Si elle voit quelque chose à faire le soir, il faut qu'elle s'en occupe tout de suite sinon ça l'empêche de s'endormir.
Au moment de dormir, son cerveau et son cœur s'emballent ce qui l'empêche de dormir malgré son manque de sommeil.
</p>
<h3>L'objectif du programme pour un dauphin</h3>
<p>Augmenter l'énergie matinale
Diminuer l'anxiété du soir afin de mieux dormir
</p>
<h3>Exemple de conseils pour un dauphin</h3>
<p>1 heure avant d'aller dormir, faire des activités relaxantes (prendre un bain chaud, lire un livre de fiction...) et arrêter les écrans.
Être le plus régulier possible sur le rythme de sommeil.</p>

<h2>Le pouvoir du Quand</h2>
<p>Quand? est un livre riche d'astuces et pleins de bons conseilles. Qui peuvent nous être très utiles dans toutes nos activités quotidiennes.
Le plus important dans ces programmes, est de réussir à tenir le rythme. Il vaut mieux être régulier et tenir un rythme constant, la semaine et le week-end. Plutôt que de faire le yoyo avec ses heures de coucher et de réveils.
On apprend aussi énormément sur ses habitudes et sur comment optimiser ses activités. Tous les conseils dans le livre sont appuyés d'explications et d'études scientifiques ce qui les rend très pertinents !
Voici certaines habitudes qui ne sont pas utilisées correctement dans nos vies et ont un impacte sur notre énergie quotidienne et notre sommeil.</p>

<h3>Soirées</h3>
<p>L'impact des grosses soirées / fêtes, se coucher plus tard à cause d'une soirée ne va jamais être anodin pour votre corps. En plus, d'être fatigué le lendemain, vous ne profiterez pas de tous les bénéfices du sommeil. Vous récupérez moins bien physiquement, vous apprendrez/retenez moins bien ce que vous avez appris ce jour...
</p>
<h3>Grasses mat !</h3>
<p>Les grasses matinées sont aussi à modérer. Il conseille de ne pas se lever plus de 45min après son heure de levé habituel.</p>
<h3>Siestes</h3>
<p>Par exemple, les siestes. Sachez que les siestes après 18h sont fortement déconseillées sous peine de se coucher plus tard, ce qui pourrait impacter son rythme de sommeil. Faire des siestes de 20min ou moins va permettre d'être de nouveau frais pour les heures qui suivent. Elles sont conseillées de le faire à divers moments de la journée suivant les chronotypes.
Une sieste de 20 ou moins va permettre d'être frais les heures qui suivent. Dépasser les 20min va permettre au corps de lancer différents processus de nettoyage. 1h de sieste va permettre au corps de récupérer physiquement pour le reste de la semaine. Mais dépasser mes 20min de sieste, le réveille sera toujours difficile pour les heures qui suivent.
</p>
<h3>Créativité</h3>
<p>Parlons de créativité, c'est peut-être un des enjeux principaux de son métier. Tout un chapitre du livre est dédié à la créativité, suivant chaque chronotype quels sont les moments où l'on est le plus créatif. Comment créer des situations pour être plus créatif... Dans le livre, vous trouverez des conseils concret pour profiter pleinement de sa créativité intérieure et surtout à quel moment y être le plus attentif.
Ce n'est pas pour rien qu'une grande partie des artistes sont des loups !
</p>
