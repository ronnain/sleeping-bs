Les conseils donnés sont faciles à mettre en place, il n'y a pas de secrets, il faut les mettre en place au fur et à mesure et apprécier le résultat.
C'est un livre que je conseille si vous trouvez manquer d'énergie ou si vous ne dormez pas bien.
</p>

